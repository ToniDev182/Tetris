public class Persona {

    int edad; // Edad de la persona
    String pais; // País de origen de la persona
    String nombre; // Nombre de la persona

    public int getEdad() {
        return edad; // Obtener la edad de la persona
    }

    public void setEdad(int e) {
        edad = e; // Establecer la edad de la persona
    }

    public String getPais() {
        return pais; // Obtener el país de origen de la persona
    }

    public void setPais(String p) {
        pais = p; // Establecer el país de origen de la persona
    }

    public String getNombre() {
        return nombre; // Obtener el nombre de la persona
    }

    public void setNombre(String n) {
        nombre = n; // Establecer el nombre de la persona
    }
}
